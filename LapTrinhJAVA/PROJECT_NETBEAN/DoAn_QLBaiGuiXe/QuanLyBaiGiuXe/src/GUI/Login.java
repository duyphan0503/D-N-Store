    private void cbxShowPWActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxShowPWActionPerformed
        // TODO add your handling code here:
         // Kiểm tra trạng thái của checkbox
        if (cbxShowPW.isSelected()) {
            // Nếu được chọn, hiển thị mật khẩu dạng chuỗi (plaintext)
            txtPW.setEchoChar((char) 0); // 0 nghĩa là không ẩn ký tự
        } else {
            // Nếu không được chọn, hiển thị mật khẩu dưới dạng ký tự ẩn (*)
            txtPW.setEchoChar('*'); // Hoặc sử dụng ký tự mặc định
        }
    }//GEN-LAST:event_cbxShowPWActionPerformed

    
