    private void AuthenticateAndLogin() {
        if (txtUsername.getText().isEmpty()) {
            txtMsg.setText("Bạn chưa nhập tên tài khoản!");
            return;
        }
        if (txtPW.getText().isEmpty()) {
            txtMsg.setText("Bạn chưa nhập mật khẩu!");
            return;
        }

        if (!LoginDAO.getInstance().Login(txtUsername.getText(), txtPW.getText())) {
            txtMsg.setText("Sai tên đăng nhập hoặc mật khẩu!!");
            return;
        }
        
        Main main = new Main();
        main.setVisible(true);
        dispose();
    }

