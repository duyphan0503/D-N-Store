    private void LoadTable() {
        tableModel.setRowCount(0);
        List<ViTri> list = ViTriDAO.getInstance().LoadListViTri();
        for (int i = 0; i < list.size(); i++) {
            ViTri vt = list.get(i);
            Object[] dt = {i + 1, vt.getIdViTri(), vt.getTenViTri(), vt.getTrangThai()};
            tableModel.addRow(dt);
        }
    }
