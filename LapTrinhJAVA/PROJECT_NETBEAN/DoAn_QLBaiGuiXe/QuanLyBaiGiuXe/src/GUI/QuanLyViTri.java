    private void tblViTriMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblViTriMouseClicked
        // TODO add your handling code here:
        int row = tblViTri.getSelectedRow();
        List<ViTri> list = ViTriDAO.getInstance().LoadListViTri();
        idSave = list.get(row).getIdViTri();

        txtID.setText(tblViTri.getValueAt(row, 1) + "");
        txtTenVT.setText(tblViTri.getValueAt(row, 2) + "");
        cbxTrangThai.setSelectedItem(tblViTri.getValueAt(row, 3).toString());
    }//GEN-LAST:event_tblViTriMouseClicked

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (txtTenVT.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa tên vị trí!");
            return;
        }
        if (cbxTrangThai.getSelectedItem().toString().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa chọn trạng thái!");
            return;
        }
        // Thêm mới tài khoản
        if (ViTriDAO.getInstance().AddViTri(txtTenVT.getText(), cbxTrangThai.getSelectedItem().toString())) {
            JOptionPane.showMessageDialog(null, "Thêm mới thành công!!");
            LoadTable(); // Hàm load lại bảng dữ liệu
        } else {
            JOptionPane.showMessageDialog(null, "Lỗi!!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String key = txtSearch.getText().trim(); // Lấy từ khóa từ ô nhập liệu
        List<ViTri> list = ViTriDAO.getInstance().SearchViTriByName(key); // Gọi hàm tìm kiếm

        // Xóa dữ liệu cũ trong bảng
        DefaultTableModel tableModel = (DefaultTableModel) this.tableModel;
        tableModel.setRowCount(0);

        // Thêm dữ liệu mới vào bảng
        for (int i = 0; i < list.size(); i++) {
            ViTri vt = list.get(i);
            Object[] dt = {i + 1, vt.getIdViTri(), vt.getTenViTri(), vt.getTrangThai()};
            tableModel.addRow(dt);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if(idSave >= 0){
            if (txtTenVT.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chưa tên vị trí!");
                return;
            }
            if (cbxTrangThai.getSelectedItem().toString().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chưa chọn trạng thái!");
                return;
            }
            // Cập nhật tài khoản
            if (ViTriDAO.getInstance().UpdateViTri(idSave, txtTenVT.getText(), cbxTrangThai.getSelectedItem().toString())) {
                JOptionPane.showMessageDialog(null, "Cập nhật thành công!!");
                LoadTable(); // Hàm load lại bảng dữ liệu
            } else {
                JOptionPane.showMessageDialog(null, "Lỗi!!");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Chọn vị trí để sửa");
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (idSave >= 0) {
            if (ViTriDAO.getInstance().DeleteViTri(idSave)) {
                LoadTable();
                idSave = -1;
                txtID.setText("");
                txtTenVT.setText("");
                JOptionPane.showMessageDialog(null, "Xóa vị trí thành công!!");
            } else {
                JOptionPane.showMessageDialog(null, "Xóa vị trí không thành công!!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Chọn để xóa!!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

