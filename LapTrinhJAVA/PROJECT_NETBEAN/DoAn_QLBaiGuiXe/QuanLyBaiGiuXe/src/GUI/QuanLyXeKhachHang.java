    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:        
        if (txtBienSoXe.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa nhập biển số xe!");
            return;
        }
        if (cbxKhachHang.getSelectedItem().toString().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa chọn khách hàng!");
            return;
        }
        if (txtLoaiXe.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa nhập loại xe!");
            return;
        }
        // Lấy idKhachHang từ combobox (giả sử là "idKhachHang - TenKhachHang")
        int idKhachHang = Integer.parseInt(cbxKhachHang.getSelectedItem().toString().split(" - ")[0]);

        // Thêm mới xe khách hàng
        if (XeKhachHangDAO.getInstance().AddXeKhachHang(txtBienSoXe.getText(), idKhachHang, txtLoaiXe.getText())) {
            JOptionPane.showMessageDialog(null, "Thêm mới thành công!!");
            LoadTable(); // Hàm load lại bảng dữ liệu
        } else {
            JOptionPane.showMessageDialog(null, "Lỗi!!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if(idSave >= 0){
            if (txtBienSoXe.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chưa nhập biển số xe!");
                return;
            }
            if (cbxKhachHang.getSelectedItem().toString().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chưa chọn khách hàng!");
                return;
            }
            if (txtLoaiXe.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chưa nhập loại xe!");
                return;
            }

            // Lấy idKhachHang từ combobox (giả sử là "idKhachHang - TenKhachHang")
            int idKhachHang = Integer.parseInt(cbxKhachHang.getSelectedItem().toString().split(" - ")[0]);

            // Cập nhật thông tin xe khách hàng
            if (XeKhachHangDAO.getInstance().UpdateXeKhachHang(idSave, txtBienSoXe.getText(), idKhachHang, txtLoaiXe.getText())) {
                JOptionPane.showMessageDialog(null, "Cập nhật thành công!!");
                LoadTable(); // Hàm load lại bảng dữ liệu
            } else {
                JOptionPane.showMessageDialog(null, "Lỗi!!");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Chọn tài khoản để sửa");
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (idSave >= 0) {
            if (XeKhachHangDAO.getInstance().DeleteXeKhachHang(idSave)) {
                LoadTable();
                idSave = -1;
                txtID.setText("");
                txtBienSoXe.setText("");
                txtLoaiXe.setText("");
                JOptionPane.showMessageDialog(null, "Xóa thông tin xe khách thành công!!");
            } else {
                JOptionPane.showMessageDialog(null, "Xóa thông tin xe khách không thành công!!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Chọn để xóa!!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

