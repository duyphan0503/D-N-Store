    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String key = txtSearch.getText().trim(); // Lấy từ khóa từ ô nhập liệu
            List<XeKhachHang> list = XeKhachHangDAO.getInstance().SearchXeKhachHangByLicensePlate(key); // Gọi hàm tìm kiếm

            // Xóa dữ liệu cũ trong bảng
            DefaultTableModel tableModel = (DefaultTableModel) this.tableModel;
            tableModel.setRowCount(0);

            for (int i = 0; i < list.size(); i++) {
            XeKhachHang xekh = list.get(i);
            Object[] dt = {i + 1, xekh.getIdXe(), xekh.getBienSoXe(), xekh.getIdKhachHang(), xekh.getLoaiXe()};
            tableModel.addRow(dt);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

