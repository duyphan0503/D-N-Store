    private void LoadTable() {
        tableModel.setRowCount(0);
        List<XeKhachHang> list = XeKhachHangDAO.getInstance().LoadListXeKhachHang();
        for (int i = 0; i < list.size(); i++) {
            XeKhachHang xekh = list.get(i);
            Object[] dt = {i + 1, xekh.getIdXe(), xekh.getBienSoXe(), xekh.getIdKhachHang(), xekh.getLoaiXe()};
            tableModel.addRow(dt);
        }
    }
    
    private void LoadCbBox() {
        // Lấy danh sách idKhachHang kèm theo tên khách hàng
        List<String> listKhachHang = XeKhachHangDAO.getInstance().LoadIdKhachHangWithName();

        // Xóa tất cả các mục hiện tại trong ComboBox
        cbxKhachHang.removeAllItems();

        // Thêm các thông tin idKhachHang và tên vào ComboBox
        for (String khachHangInfo : listKhachHang) {
            cbxKhachHang.addItem(khachHangInfo); // Ví dụ: "1 - Nguyễn Văn A"
        }
    }

