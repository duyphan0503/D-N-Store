    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String key = txtSearch.getText().trim(); // Lấy từ khóa từ ô nhập liệu
        List<TaiKhoan> list = LoginDAO.getInstance().SearchByName(key); // Gọi hàm tìm kiếm

        // Xóa dữ liệu cũ trong bảng
        DefaultTableModel tableModel = (DefaultTableModel) this.tableModel;
        tableModel.setRowCount(0);

        // Thêm dữ liệu mới vào bảng
        for (int i = 0; i < list.size(); i++) {
            TaiKhoan account = list.get(i);
            Object[] dt = {i + 1, account.getIdTaiKhoan(), account.getTenDangNhap(), account.getVaiTro()};
            tableModel.addRow(dt);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

