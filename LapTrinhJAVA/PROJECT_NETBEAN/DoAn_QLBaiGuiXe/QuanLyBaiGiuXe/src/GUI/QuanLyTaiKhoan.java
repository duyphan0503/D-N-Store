    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (idSave >= 0) {
            if (LoginDAO.getInstance().Delete(idSave)) {
                LoadTable();
                idSave = -1;
                txtID.setText("");
                txtTenTK.setText("");
                txtPW.setText("");
                JOptionPane.showMessageDialog(null, "Xóa tài khoản thành công!!");
            } else {
                JOptionPane.showMessageDialog(null, "Xóa tài khoản không thành công!!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Chọn để xóa!!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if(idSave >= 0){
            if (txtTenTK.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chưa nhập tài khoản!");
                return;
            }
            if (txtPW.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chưa nhập mật khẩu!");
                return;
            }
            if (cbxVaiTro.getSelectedItem().toString().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chưa nhập vai trò!");
                return;
            }
            // Cập nhật tài khoản
            if (LoginDAO.getInstance().Update(idSave, txtTenTK.getText(), txtPW.getText(), cbxVaiTro.getSelectedItem().toString())) {
                JOptionPane.showMessageDialog(null, "Cập nhật thành công!!");
                LoadTable(); // Hàm load lại bảng dữ liệu
            } else {
                JOptionPane.showMessageDialog(null, "Lỗi!!");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Chọn tài khoản để sửa");
        }
    }//GEN-LAST:event_btnEditActionPerformed

