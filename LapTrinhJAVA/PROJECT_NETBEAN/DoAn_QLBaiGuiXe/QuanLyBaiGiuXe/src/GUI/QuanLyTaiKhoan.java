    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (txtTenTK.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa nhập tài khoản!");
            return;
        }
        if (txtPW.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa nhập mật khẩu!");
            return;
        }
        if (cbxVaiTro.getSelectedItem().toString().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa nhập vai trò!");
            return;
        }
        // Thêm mới tài khoản
        if (LoginDAO.getInstance().Add(txtTenTK.getText(), txtPW.getText(), cbxVaiTro.getSelectedItem().toString())) {
            JOptionPane.showMessageDialog(null, "Thêm mới thành công!!");
            LoadTable(); // Hàm load lại bảng dữ liệu
        } else {
            JOptionPane.showMessageDialog(null, "Lỗi!!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void tblTaiKhoanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTaiKhoanMouseClicked
        // TODO add your handling code here:
        int row = tblTaiKhoan.getSelectedRow();
            List<TaiKhoan> list = LoginDAO.getInstance().listTaiKhoan();
            idSave = list.get(row).getIdTaiKhoan();

            txtID.setText(tblTaiKhoan.getValueAt(row, 1) + "");
            txtTenTK.setText(tblTaiKhoan.getValueAt(row, 2) + "");
            cbxVaiTro.setSelectedItem(tblTaiKhoan.getValueAt(row, 3).toString());
    }//GEN-LAST:event_tblTaiKhoanMouseClicked

