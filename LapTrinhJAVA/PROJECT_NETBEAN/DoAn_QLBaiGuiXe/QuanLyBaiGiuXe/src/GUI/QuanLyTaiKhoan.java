    private void LoadTable() {
        tableModel.setRowCount(0);
        List<TaiKhoan> list = LoginDAO.getInstance().listTaiKhoan();
        for (int i = 0; i < list.size(); i++) {
            TaiKhoan account = list.get(i);
            Object[] dt = {i + 1, account.getIdTaiKhoan(), account.getTenDangNhap(), account.getVaiTro()};
            tableModel.addRow(dt);
        }
    }

