    private void tblKHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKHMouseClicked
        // TODO add your handling code here:
        int row = tblKH.getSelectedRow();
        List<KhachHang> list = KhachHangDAO.getInstance().LoadListKhachHang();
        idSave = list.get(row).getIdKhachHang();

        txtID.setText(tblKH.getValueAt(row, 1) + "");
        txtTenKH.setText(tblKH.getValueAt(row, 2) + "");
        txtSDT.setText(tblKH.getValueAt(row, 3) + "");
        txtDiaChi.setText(tblKH.getValueAt(row, 4) + "");
    }//GEN-LAST:event_tblKHMouseClicked

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (txtTenKH.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa nhập tên khách hàng!");
            return;
        }
        if (txtSDT.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa nhập số điện thoại!");
            return;
        }
        if (txtDiaChi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa nhập địa chỉ!");
            return;
        }
        // Thêm mới tài khoản
        if (KhachHangDAO.getInstance().AddKhachHang(txtTenKH.getText(), txtSDT.getText(), txtDiaChi.getText())) {
            JOptionPane.showMessageDialog(null, "Thêm mới thành công!!");
            LoadTable(); // Hàm load lại bảng dữ liệu
        } else {
            JOptionPane.showMessageDialog(null, "Lỗi!!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if(idSave >= 0){
            if (txtTenKH.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chưa nhập tài khoản!");
                return;
            }
            if (txtSDT.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chưa nhập mật khẩu!");
                return;
            }
            if (txtDiaChi.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Chưa nhập địa chỉ!");
                return;
            }
            if (KhachHangDAO.getInstance().UpdateKhachHang(idSave, txtTenKH.getText(), txtSDT.getText(), txtDiaChi.getText())) {
                JOptionPane.showMessageDialog(null, "Cập nhật thành công!!");
                LoadTable(); // Hàm load lại bảng dữ liệu
            } else {
                JOptionPane.showMessageDialog(null, "Lỗi!!");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Chọn khách hàng để sửa");
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (idSave >= 0) {
            if (KhachHangDAO.getInstance().DeleteKhachHang(idSave)) {
                LoadTable();
                idSave = -1;
                txtID.setText("");
                txtTenKH.setText("");
                txtSDT.setText("");
                txtDiaChi.setText("");
                JOptionPane.showMessageDialog(null, "Xóa khách hàng thành công!!");
            } else {
                JOptionPane.showMessageDialog(null, "Xóa khách hàng không thành công!!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Chọn để xóa!!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

