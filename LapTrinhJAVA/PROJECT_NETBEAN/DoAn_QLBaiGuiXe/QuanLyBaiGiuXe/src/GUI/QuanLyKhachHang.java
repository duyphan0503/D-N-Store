    private void LoadTable() {
        tableModel.setRowCount(0);
        List<KhachHang> list = KhachHangDAO.getInstance().LoadListKhachHang();
        for (int i = 0; i < list.size(); i++) {
            KhachHang kh = list.get(i);
            Object[] dt = {i + 1, kh.getIdKhachHang(), kh.getHoTen(), kh.getSoDienThoai(), kh.getDiaChi()};
            tableModel.addRow(dt);
        }
    }

